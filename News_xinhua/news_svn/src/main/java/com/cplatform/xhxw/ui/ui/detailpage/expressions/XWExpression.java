package com.cplatform.xhxw.ui.ui.detailpage.expressions;

public class XWExpression {
	/**
	 * 表情资源文件ID
	 */
	private int mImgResId;
	/**
	 * 用于替换表情的文字信息
	 */
	private String mExprInfo;
	
	public int getmImgResId() {
		return mImgResId;
	}
	public void setmImgResId(int mImgResId) {
		this.mImgResId = mImgResId;
	}
	public String getmExprInfo() {
		return mExprInfo;
	}
	public void setmExprInfo(String mExprInfo) {
		this.mExprInfo = mExprInfo;
	}
}
